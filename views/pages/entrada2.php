        <h2><strong>Ojo a los huecos</strong></h2>
        <p>
            Cuando disponemos de poca holgura en casa,
            hay que echarle imaginación para que cualquier rincón pueda
            ser utilizable. Aprovechar los huecos que encontramos en cada
            rincón es una manera eficaz de liberar espacio con un almacenaje que,
            a su vez, no ocupa espacio. Esto es, poner cajas, cestos, o similares en
            el hueco que hay debajo de la mesita auxiliar,
            de la cama, o incluso de la escalera, si la tenemos.</p>
